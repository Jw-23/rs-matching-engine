// ============================================================
// 量化交易订单簿 (Order Book) 核心数据结构
// ============================================================
// 本模块定义了订单簿中最基础的数据类型，包括订单标识、价格、买卖方向、订单类型
// 以及完整的订单结构体。这些类型构成了整个交易引擎的原子单元。

use std::num::NonZeroU64;

// ------------------------------------------------------------
// 交易品种标识符 (SymbolId)
// ------------------------------------------------------------
/// 交易品种的唯一标识符，如 "BTC/USDT" 映射为 `SymbolId = 1`。
/// 同一订单簿只能处理单一品种，所有订单必须有相同的 `SymbolId`。
pub type SymbolId = u64;

// ------------------------------------------------------------
// 订单数量 (Quantity)
// ------------------------------------------------------------
/// 订单数量，以最小交易单位的整数倍表示（如 1 股、0.001 BTC）。
/// 使用整数避免浮点精度问题。部分成交时此值会逐步减少。
pub type Quantity = u64;

// ------------------------------------------------------------
// 订单标识符 (OrderId)
// ------------------------------------------------------------
/// 订单的唯一数字标识符，使用无符号64位整数 (`u64`) 表示。
///
/// 在量化交易系统中，每个订单必须拥有全局唯一的 ID，用于：
/// - 订单生命周期的追踪（创建 → 成交 → 撤销）
/// - 订单与成交记录的关联
/// - 撮合引擎内部的匹配查找
///
/// 在实际生产系统中，OrderId 通常由以下方式生成：
///   - 单调递增的原子计数器（高性能场景）
///   - 分布式 ID 生成器（如 Snowflake 算法，用于多节点部署）
pub type OrderId = u64;
/// 务必注意价格不能为0，不然会报错
/// 在金融场景中，所有价格都必须精确表示：
///   - 股票：可能精确到分（0.01元）
///   - 外汇：可能精确到 0.00001（5位小数）
///   - 加密货币：可能精确到 0.00000001（8位小数）
///
/// 整数存储的优势：
///   - 精确比较，无精度误差
///   - 整数运算比浮点运算更快
///   - 避免 NaN、Infinity 等边界值问题
///
/// 【注意】当前实现假设价格已按资产最小单位标准化。

#[derive(Debug, PartialEq, PartialOrd, Eq, Clone, Copy, Default)]
pub struct Price(pub Option<NonZeroU64>);
impl Into<Price> for NonZeroU64 {
    fn into(self) -> Price {
        Price(Some(self))
    }
}
impl Into<Price> for u64 {
    fn into(self) -> Price {
        Price(Some(NonZeroU64::new(self)).unwrap())
    }
}
impl Into<Price> for Option<u64> {
    fn into(self) -> Price {
        Price(self.map(|x| NonZeroU64::new(x).unwrap()))
    }
}
impl Price {
    fn to_usize(&self) -> Option<usize> {
        self.0.map(|x| x.get() as usize)
    }
}
// ------------------------------------------------------------
// 订单方向 (Side)
// ------------------------------------------------------------
/// 订单的买卖方向，决定了订单在订单簿中的挂单位置。
///
/// ## 市价撮合规则
/// - **买单 (Buy)**：在订单簿中按**价格降序**排列，最高买价在顶部
/// - **卖单 (Sell)**：在订单簿中按**价格升序**排列，最低卖价在顶部
///
/// ## 撮合条件
/// 当最佳买价 >= 最佳卖价时，市场存在套利机会，触发撮合：
///   `best_bid_price >= best_ask_price` → 执行交易
///
/// ## 典型用法
/// ```rust,ignore
/// if order.side == Side::Buy && order.price >= best_ask {
///     // 可以立即成交
/// }
/// ```
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub enum Side {
    /// 买单（买入资产）：交易者愿意以此价格买入
    #[default]
    Buy,
    /// 卖单（卖出资产）：交易者愿意以此价格卖出
    Sell,
}
// Side 枚举的辅助方法
impl Side {
    /// 返回对手盘方向
    #[inline(always)]
    pub fn opposite(&self) -> Self {
        match self {
            Side::Buy => Side::Sell,
            Side::Sell => Side::Buy,
        }
    }
}
// ------------------------------------------------------------
// 订单类型 (OrderType)
// ------------------------------------------------------------
/// 订单的执行类型，决定了撮合引擎如何处理该订单。
///
/// ## 限价单 vs 市价单
///
/// ### Limit（限价单）
/// - 交易者指定一个**期望价格**
/// - 如果市场当前价格优于等于指定价格，立即成交
/// - 否则，订单进入订单簿等待对手方
/// - 特点：**不会产生更差的成交价格**，但可能无法成交
///
/// ### Market（市价单）
/// - 不指定价格，以**市场当前最优价**立即成交
/// - 买单以最低卖价成交，卖单以最高买价成交
/// - 特点：**保证成交**，但价格不确定（滑点风险）
/// - 通常用于追求执行速度而非最优价格
#[derive(Debug, Clone, Copy, Default)]
pub enum OrderType {
    /// 限价单 (Limit Order)
    /// - 指定 `price` 字段作为成交价格上限（买单）或下限（卖单）
    /// - 可部分成交：订单可以被拆分为多笔小订单在不同价格成交
    Limit,
    /// 市价单 (Market Order)
    /// - 以市场最优价立即成交，不使用 `price` 字段
    /// - 在流动性不足的市场中可能出现较大滑点
    #[default]
    Market,
}

// ------------------------------------------------------------
// 订单 (Order)
// ------------------------------------------------------------
/// 量化交易系统中的完整订单结构体。
///
/// 订单是撮合引擎处理的基本单元。一个订单从创建到消亡会经历以下状态：
///   创建 → 提交 → [撮合中] → 部分成交 / 完全成交 / 撤销 / 拒绝
///
/// ## 字段说明
///
/// ### id (OrderId)
/// 系统分配的全局唯一订单ID，用于内部追踪和引用。
///
/// ### price (Price)
/// 限价单的价格。对于市价单此字段意义不大。
///
/// ### client_order_id (OrderId)
/// 客户端自定义的订单标识，用于关联交易终端的本地记录。
/// 重要：此 ID 在客户端范围内唯一，而非全局唯一。
/// 用途：客户端在收到成交回报时，通过此字段找到对应的本地订单。
///
/// ### symbol_id (u64)
/// 交易品种标识符，如 "BTC/USDT" 可能映射为 `symbol_id = 1`。
/// 同一订单簿只能处理单一品种，所有订单必须有相同的 `symbol_id`。
///
/// ### quantity (u64)
/// 订单数量，以最小交易单位（如 1 股、0.001 BTC）的整数倍表示。
/// 使用整数避免浮点精度问题。部分成交时，此值会逐步减少。
///
/// ### side (u64)
/// 订单方向：
///   - 当前使用裸 `u64` 表示，建议改为 `Side` 枚举类型以提高类型安全性
///   - 0 = 买单, 1 = 卖单（或反过来，取决于业务定义）
///
/// ### ty (OrderType)
/// 订单类型：`Limit` 或 `Market`。
///
/// ### active (bool)
/// 订单是否处于活跃状态。
/// - `true`：订单仍在订单簿中，等待撮合或部分成交
/// - `false`：订单已完全成交、已撤销、或被系统拒绝，不再参与撮合
///
/// ## 订单生命周期示例
/// ```text
/// 用户提交限价买单: price=100, qty=1000, active=true
///                              ↓
/// 价格下跌至 100: 立即成交 500 股
///   → qty=500, active=true  (仍有部分挂单)
///                              ↓
/// 价格再次下跌至 100: 剩余 500 股全部成交
///   → qty=0, active=false   (完全成交)
/// ```
#[derive(Debug, Clone, Copy, Default)]
pub struct Order {
    /// 系统分配的全局唯一订单ID
    pub id: OrderId,
    /// 订单价格（整数标准化）
    pub price: Price,
    /// 客户端自定义订单ID（用于回报关联）
    pub client_order_id: OrderId,
    /// 交易品种标识符
    pub symbol_id: SymbolId,
    /// 订单数量（整数标准化）
    pub quantity: Quantity,
    /// 订单方向（0=买, 1=卖），建议改为 Side 枚举
    pub side: Side,
    /// 订单类型：限价单或市价单
    pub ty: OrderType,
    /// 订单是否仍活跃（未完全成交/撤销）
    pub active: bool,
}
